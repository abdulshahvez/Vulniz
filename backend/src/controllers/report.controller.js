import PDFDocument from 'pdfkit';
import jobQueue from '../queue/job-queue.js';
import logger from '../utils/logger.js';

/**
 * Report Controller
 * ─────────────────
 * Generates exportable PDF security reports for completed scans.
 */

/**
 * GET /api/report/:id — Get JSON report
 */
export function getReport(req, res) {
  const job = jobQueue.getJob(req.params.id);
  if (!job) {
    return res.status(404).json({ error: 'Scan not found.' });
  }
  if (job.status !== 'completed') {
    return res.status(400).json({ error: 'Scan is not yet complete.' });
  }

  res.json({
    scanId: job.id,
    targetUrl: job.targetUrl,
    scannedAt: job.completedAt,
    score: job.score,
    analysis: job.analysis,
    fixes: job.fixes,
    results: job.results,
  });
}

/**
 * GET /api/report/:id/pdf — Download PDF report
 */
export function downloadPdfReport(req, res) {
  const job = jobQueue.getJob(req.params.id);
  if (!job) {
    return res.status(404).json({ error: 'Scan not found.' });
  }
  if (job.status !== 'completed') {
    return res.status(400).json({ error: 'Scan is not yet complete.' });
  }

  try {
    const doc = new PDFDocument({ margin: 50, size: 'A4' });

    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader(
      'Content-Disposition',
      `attachment; filename="security-report-${job.id.substring(0, 8)}.pdf"`,
    );
    doc.pipe(res);

    // ── Title page ─────────────────────────────────────────────────
    doc
      .fontSize(28)
      .font('Helvetica-Bold')
      .fillColor('#1a1a2e')
      .text('API Security Report', { align: 'center' });
    doc.moveDown(0.5);
    doc
      .fontSize(12)
      .font('Helvetica')
      .fillColor('#666')
      .text(`Generated by Vulniz`, { align: 'center' });
    doc.moveDown(2);

    // ── Scan info ──────────────────────────────────────────────────
    doc.fontSize(11).fillColor('#333');
    doc.text(`Target URL: ${job.targetUrl}`);
    doc.text(`Scan Date: ${new Date(job.completedAt).toLocaleString()}`);
    doc.text(`Scan ID: ${job.id}`);
    doc.moveDown(1.5);

    // ── Score ──────────────────────────────────────────────────────
    const score = job.score;
    doc.fontSize(20).font('Helvetica-Bold');
    const scoreColor =
      score.score >= 90 ? '#00aa55' :
      score.score >= 75 ? '#88aa00' :
      score.score >= 50 ? '#cc8800' :
      score.score >= 25 ? '#cc4400' : '#cc0033';
    doc.fillColor(scoreColor).text(`Security Score: ${score.score}/100`, { align: 'center' });
    doc.fontSize(14).fillColor('#666').text(`Risk Level: ${score.riskLevel}`, { align: 'center' });
    doc.moveDown(0.5);
    doc.fontSize(11).font('Helvetica').fillColor('#555').text(score.riskDescription, { align: 'center' });
    doc.moveDown(1.5);

    // ── Summary ────────────────────────────────────────────────────
    doc
      .fontSize(16)
      .font('Helvetica-Bold')
      .fillColor('#1a1a2e')
      .text('Vulnerability Summary');
    doc.moveDown(0.5);
    doc.fontSize(11).font('Helvetica').fillColor('#333');
    doc.text(`Total Vulnerabilities: ${score.breakdown.total}`);
    doc.text(`Critical: ${score.breakdown.critical}`);
    doc.text(`High: ${score.breakdown.high}`);
    doc.text(`Medium: ${score.breakdown.medium}`);
    doc.text(`Low: ${score.breakdown.low}`);
    doc.moveDown(1.5);

    // ── Findings detail ────────────────────────────────────────────
    doc
      .fontSize(16)
      .font('Helvetica-Bold')
      .fillColor('#1a1a2e')
      .text('Detailed Findings');
    doc.moveDown(0.5);

    for (const result of job.results) {
      if (result.findings.length === 0) continue;

      doc.addPage();
      const sevColor =
        result.severity === 'HIGH' ? '#cc0033' :
        result.severity === 'MEDIUM' ? '#cc8800' : '#0066cc';

      doc.fontSize(14).font('Helvetica-Bold').fillColor(sevColor);
      doc.text(`${result.attackType.toUpperCase()} — ${result.severity}`);
      doc.moveDown(0.3);
      doc.fontSize(10).font('Helvetica').fillColor('#555');
      doc.text(`Findings: ${result.findings.length} | Duration: ${result.durationMs || 0}ms`);
      doc.moveDown(0.5);

      for (const [idx, finding] of result.findings.entries()) {
        doc.fontSize(11).font('Helvetica-Bold').fillColor('#333');
        doc.text(`#${idx + 1}: ${finding.description || finding.test || finding.payload?.substring(0, 60) || 'Finding'}`);
        doc.moveDown(0.2);
        doc.fontSize(10).font('Helvetica').fillColor('#555');

        if (finding.indicators) {
          for (const indicator of finding.indicators) {
            doc.text(`  • ${indicator}`);
          }
        }
        if (finding.recommendation) {
          doc.fillColor('#006600').text(`  ✓ Fix: ${finding.recommendation}`);
        }
        doc.moveDown(0.5);
      }
    }

    // ── Recommendations ────────────────────────────────────────────
    if (score.recommendations?.length > 0) {
      doc.addPage();
      doc
        .fontSize(16)
        .font('Helvetica-Bold')
        .fillColor('#1a1a2e')
        .text('Recommendations');
      doc.moveDown(0.5);

      for (const rec of score.recommendations) {
        doc.fontSize(12).font('Helvetica-Bold').fillColor('#333');
        doc.text(`${rec.priority}. ${rec.title}`);
        doc.fontSize(10).font('Helvetica').fillColor('#555');
        doc.text(rec.description);
        if (rec.owasp) {
          doc.fillColor('#0066cc').text(`OWASP: ${rec.owasp}`);
        }
        doc.moveDown(0.5);
      }
    }

    // ── Fix Suggestions ────────────────────────────────────────────
    if (job.fixes && Object.keys(job.fixes).length > 0) {
      doc.addPage();
      doc
        .fontSize(16)
        .font('Helvetica-Bold')
        .fillColor('#1a1a2e')
        .text('Fix Suggestions');
      doc.moveDown(0.5);

      for (const [type, fix] of Object.entries(job.fixes)) {
        doc.fontSize(12).font('Helvetica-Bold').fillColor('#333');
        doc.text(fix.title || type);
        doc.fontSize(10).font('Helvetica').fillColor('#555');
        doc.text(fix.description || '');
        doc.moveDown(0.3);

        if (fix.bestPractices) {
          for (const bp of fix.bestPractices) {
            doc.text(`  • ${bp}`);
          }
        }
        doc.moveDown(0.8);
      }
    }

    // ── Footer ─────────────────────────────────────────────────────
    doc.addPage();
    doc.fontSize(10).font('Helvetica').fillColor('#999');
    doc.text('This report was generated by Vulniz.', { align: 'center' });
    doc.text('For ethical security testing only.', { align: 'center' });
    doc.text(`© ${new Date().getFullYear()} Vulniz`, { align: 'center' });

    doc.end();
    logger.info(`PDF report generated for scan ${job.id}`);
  } catch (err) {
    logger.error('PDF generation failed:', err);
    res.status(500).json({ error: 'Failed to generate PDF report.' });
  }
}
